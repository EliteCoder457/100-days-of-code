// Write a program to swap two numbers without using a third variable.
#include <stdio.h>

int main() {
float x, y;
printf("Enter the first number: ");
scanf("%f",&x);
printf("Enter the second number: ");
scanf("%f",&y);
printf("After swap first number is: %0.1f\n", y);
printf("After swap second number is : %0.1f\n",x);

    return 0;
}
